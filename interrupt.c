/******************************************************************************
* Name: interrupt_config()
* Description:  The interrupt Driver configuration is done here.

* Arguments: None
* Returns: None
******************************************************************************/

void interrupt_config()
{
    EICRA &= ~(1 << ISC10);
    EICRA |= (1 << ISC11); //any logic change of INT1 generates an interrupt
    EIMSK |= (1 << INT1);
    sei();
}
ISR(INT1_vect)
{

    FLAG_BIT.FLAG_ISR1 = 1;

}


