/******************************************************************************
* Name: adc_config()
* Description:adc is set here.
* Arguments: None
* Returns: None
******************************************************************************/

void adc_config()
{
    ADMUX &= 0x00;
    ADMUX |= (1 << REFS0);
    ADCSRA |= (1 << ADEN);
    sei();
}
/******************************************************************************
* Name: adc()
* Description:adc is configured here
* Arguments: None
* Returns: None
******************************************************************************/
void adc()
{
    uint16_t ADC_value;
    ADCSRA |= (1 << ADSC);
    while (ADCSRA & (1 << ADSC));
    ADC_value = ADC;
}

